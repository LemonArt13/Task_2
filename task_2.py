# Зчитуємо введене число
num = input("Введіть ціле число ")
# Перевіряємо чи введене число є цілим
if num.isdigit():
    num_str = str(num)
# Якщо введене число є цілим, вивиодимо гістограму
    for digit in num_str:
        k = int(digit)
        print("#"*k)
# Якщо ж число не є цілим, виводимо помилку        
else:
    print("Введено не ціле число \n ERROR!")